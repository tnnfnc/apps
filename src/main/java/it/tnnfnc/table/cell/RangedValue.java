package it.tnnfnc.table.cell;

public class RangedValue {

	private int gauge = 0;
	private int min = 0;
	private int max = 0;
	private int step = 0;
	private Object value;

	/**
	 * @return the value
	 */
	public Object getValue() {
		return value;
	}

	/**
	 * @param value
	 *            the value to set
	 */
	public void setValue(Object value) {
		this.value = value;
	}

	/**
	 * @return the gauge
	 */
	public int getGauge() {
		return gauge;
	}

	/**
	 * @param gauge
	 *            the gauge to set
	 */
	public void setGauge(int gauge) {
		this.gauge = gauge;
	}

	/**
	 * @return the min
	 */
	public int getMin() {
		return min;
	}

	/**
	 * @param min
	 *            the min to set
	 */
	public void setMin(int min) {
		this.min = min;
	}

	/**
	 * @return the max
	 */
	public int getMax() {
		return max;
	}

	/**
	 * @param max
	 *            the max to set
	 */
	public void setMax(int max) {
		this.max = max;
	}

	/**
	 * @return the step
	 */
	public int getStep() {
		return step;
	}

	/**
	 * @param step
	 *            the step to set
	 */
	public void setStep(int step) {
		this.step = step;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		return (obj != null && obj instanceof RangedValue && ((RangedValue) obj).gauge == gauge);
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		// return this.getClass().getName() + " [level = " + getGauge()
		// + "; value = " + getValue() + "]";
		int percentage = (getMax() - getMin()) == 0 ? 1 : getMax() - getMin();
		return (getValue() == null ? "" : (getValue() + " "))
				+ (percentage == 1 ? getGauge() + ""
						: (100 / percentage * getGauge()) + "%");
	}
}

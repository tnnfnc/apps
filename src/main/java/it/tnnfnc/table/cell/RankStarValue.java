package it.tnnfnc.table.cell;

/**
 * @author franco
 * 
 */
public class RankStarValue implements Comparable<RankStarValue>, Cloneable {
	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#clone()
	 */
	@Override
	protected Object clone() throws CloneNotSupportedException {
		RankStarValue v = new RankStarValue();
		v.level = this.level;
		return v;
	}

	/**
	 * Default max scale: 5.
	 */
	public static final int SCALE = 5;
	private int level = 0;

	/**
	 * @return the level
	 */
	public int getLevel() {
		return level;
	}

	/**
	 * @param level
	 *            the level to set
	 */
	public void setLevel(int level) {
		this.level = level;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		// return this.getClass().getName() + " [rating = " + level + "]";
		return "[" + level + "/" + SCALE + "]";
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		return obj instanceof RankStarValue
				&& ((RankStarValue) obj).level == level;
	}

	@Override
	public int compareTo(RankStarValue o) {
		return this.level - o.level;
	}

}
